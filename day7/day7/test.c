//猜数字游戏
int guess = 0;
void menu()
{
	printf("############################\n");
	printf("####  1.play   2.exit  #####\n");
	printf("############################\n");

}
void game()
{
	int ret = 0;
	ret= rand()%100+1;
	while (1)
	{
		printf("请猜数字：>");
		while (scanf("%d", &guess) == 0)
		{
			while (getchar() != '\n');
			printf("您输入了非法字符，请重新输入：\n");
		}
		
		if (guess > ret)
		{
			printf("大了\n");
		}
		else if (guess < ret)
		{
			printf("小了\n");
		}
		else if (guess == ret)
		{
			printf("猜对了！\n\n\n");
			break;
		}
		
	}
		
}

int main()
{
	int input = 0;
	srand((unsigned int)time(NULL));
	do
	{
		menu();
		printf("请选择>:");
		scanf("%d", &input);
		switch (input)
		{
		case 1:
			game();
			break;
		case 2:
			printf("退出游戏\n");
			break;
		default:
			printf("选择无效\n");
			break;
		}
	} while (input);
	return 0;
}




